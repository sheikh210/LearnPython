computer_parts = ["pc",
                  "monitor",
                  "keyboard",
                  "mouse",
                  "mouse pad"
                  ]

# Iterating over list
for part in computer_parts:
    print(part)

print()

# Printing an item in the list using index value
print(computer_parts[2])

# Slicing a list - Output will be another list
print(computer_parts[0:3])
print(computer_parts[-2])
