# Basic sequence types in Python include Strings, Lists, Tuples, Ranges & Byte/ByteArrays
# Sequences are ordered collections of items
# If a sequence wasn't ordered, then we couldn't refer to individual items contained within them via index

# In Python, anything you can iterate over (using a for-loop), is iterable
# All sequence types are iterable, but not all iterables are sequences
# You can use a dictionary in a for-loop, but dictionary is not a sequence type
